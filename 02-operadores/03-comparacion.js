let a = 10;

//Operadores Relacionales
console.log(a > 5); // a mayor que 5
console.log(a >= 5);// a mayor o igual que 5
console.log(a < 5);// a menor que 5
console.log(a <= 5); // a menor o igual que 5

//Operadores de Igualdad
console.log(a == 10); // a es igual a 10  (Evitar usar estos, fuente de errores)
console.log(a != 10);// a es distinto a 10  (Evitar usar estos, fuente de errores)
console.log(a === '10'); // a es igual que 10 + a tiene el mismo tipo de dato que 10
console.log(a !== '10'); // a es igual que 10 + a tiene distinto tipo de dato que 10
